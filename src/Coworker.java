public class Coworker {
    private String name;
    private String office;
    private String mail;
    private String phone;
    private float salary;
    private int age;

    public Coworker (String name, String office, String mail, String phone, float salary, int age){
        this.name = name;
        this.office = office;
        this.mail = mail;
        this.phone = phone;
        this.salary = salary;
        this.age = age;
    }
}
